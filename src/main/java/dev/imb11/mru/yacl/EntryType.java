/**
 * Represents the type of text for an option.
 */
public enum EntryType {
    // Represents a category name.
    CATEGORY_NAME,
    // Represents a group name.
    GROUP_NAME,
    // Represents an option name.
    OPTION_NAME,
    // Represents an option description.
    OPTION_DESCRIPTION,
}
